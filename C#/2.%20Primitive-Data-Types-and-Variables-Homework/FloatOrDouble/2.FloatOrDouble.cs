using System;

namespace FloatOrDouble
{
    class FloatOrDouble
    {
        static void Main()
        {
            float  firstFloatNumber = 12.345f;
            double doubleNumber = 34.567839023d;
            double secondDoubleNumber = 8923.1234857d;
            float secondFloatNumber = 3456.091f;
            Console.WriteLine("The first float number is: {0} \nThe second float number  is : {1}", firstFloatNumber, secondFloatNumber);
            Console.WriteLine("The first double number is: {0}\nThe second double number is: {1}", doubleNumber, secondDoubleNumber);
        }
    }
}
